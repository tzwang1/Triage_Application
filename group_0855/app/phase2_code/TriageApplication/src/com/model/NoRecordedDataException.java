/**
 * This exception is thrown when an attempt is made to access a patient's
 * recorded data when there are no entries in it.
 * @author Afsheen
 */
package com.model;
public class NoRecordedDataException extends Exception {

	private static final long serialVersionUID = 2930661489286818376L;

	/**
	 * Initialize a general NoRecordedDataException.
	 */
	public NoRecordedDataException() {
		super();
	}

	/**
	 * Initialize a NRD exception with a custom message.
	 * @param message the message tied to the exception.
	 */
	public NoRecordedDataException(String message) {
		super(message);
	}
}
